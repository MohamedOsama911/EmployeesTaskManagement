    private void DisplaybtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DisplaybtnActionPerformed
        // TODO add your handling code here:
        if(Key==null){  
            JOptionPane.showMessageDialog(this, "Select Employee To Display His Tasks","Error",JOptionPane.ERROR_MESSAGE, new javax.swing.ImageIcon(getClass().getResource("/icons/close_30_decline.png")));
        }
        else if(empsList.get(Key).list.isEmpty() && empsList.get(Key).Finished_List.isEmpty()){
            JOptionPane.showMessageDialog(this, "Selected Employee Doesn’t Have Any Tasks","No Data",JOptionPane.INFORMATION_MESSAGE,  new javax.swing.ImageIcon(getClass().getResource("/icons/warning_30.png")));
        }
        else{
            DisplayData Display=new DisplayData(Key,Name); //call new form
            Display.setVisible(true);
            this.setVisible(false);
        }
        
    }//GEN-LAST:event_DisplaybtnActionPerformed

