    private void DeleteEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteEmpActionPerformed
        if(Key==null){  
            JOptionPane.showMessageDialog(this, "Select Employee To Delete","Error",JOptionPane.ERROR_MESSAGE, new javax.swing.ImageIcon(getClass().getResource("/icons/close_30_decline.png")));
        }
        else{
            // insert delete employee function here
            if(!postponedTasksList.isEmpty())
            {
                for(int i=0;i<postponedTasksList.size();i++)
                {
                    if(postponedTasksList.get(i).employeeEmail.equals(Key))
                    {
                        postponedTasksList.remove(i);
                        i--;
                    }
                }
            }
            empsList.get(Key).list.clear();
            empsList.get(Key).Finished_List.clear();
            empsList.remove(Key);
            tblModel.removeRow(row);
            Key=null;
            Name=null;
            row=-1;
            if (postponedTasksList.isEmpty()) {
                approveTask.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icons/notification (1).png")));
                approveTask.setToolTipText(null);
            }
        }
    }//GEN-LAST:event_DeleteEmpActionPerformed
